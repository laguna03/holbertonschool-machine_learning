def cat_arrays(arr1, arr2):
    """
    Function that concatenates two arrays.

    Args:
        arr1 (list): The first array.
        arr2 (list): The second array.

    Returns:
        list: A new list containing the elements of arr1 followed by the
              elements of arr2.
    """
    return arr1 + arr2
