"""
Module 10-ill_use_my_scale
Provides a function np_shape to get the shape of a numpy matrix.
"""

def np_shape(matrix):
    """
    Returns the shape of a numpy matrix.

    Args:
        matrix (numpy.ndarray): The matrix for which to find the shape.

    Returns:
        tuple: A tuple representing the dimensions of the matrix.
    """
    return matrix.shape
